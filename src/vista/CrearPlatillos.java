    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        int tipoDeComida=jComboBoxtipoDeComida.getSelectedIndex();
        switch (tipoDeComida) {
           case 0: 
               CrearComidaMexicana ventana=new CrearComidaMexicana();
               ventana.setVisible(true);
               this.dispose();
               break;
           case 1:
               Crear_comida_italiana ventana1=new Crear_comida_italiana();
               ventana1.setVisible(true);
               this.dispose();
               break;
           case 2:
               Crear_comida_japonesa ventana2=new Crear_comida_japonesa();
               ventana2.setVisible(true);
               this.dispose();
               break;
           default:
               throw new AssertionError();
           }
    }//GEN-LAST:event_AgregarActionPerformed

